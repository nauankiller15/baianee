                <div data-aos="fade-right" data-aos-duration="1000">
                    <h3 class="sticky-heading">Serviços</span></h3>
                </div>
            </div>
            <div data-aos="fade-left" data-aos-duration="900">
                <div class="content-half">
                    <div class="step" style="opacity: 1;">
                        <div class="steps-icon"><img src="https://1.bp.blogspot.com/-ava_qP119Gc/YE1GPn2g1JI/AAAAAAAAG2I/iax8QLR-H3wGtTI1ujAOz92QzMIEFAoZwCLcBGAsYHQ/s320/laptop.png" height="70" alt=""></div>
                        <div class="headline-7">Gestão de Tráfego</div>
                        <p>A gestão de Tráfego ou Tráfego pago é o serviço que
                            promove visitantes que chegam até um site,
                            loja ou página por meio de anúncios publicitários em plataformas específicas.
                            Com isto, é preciso o uso de estratégias de marketing digital,
                            por um profissional competente, para que a circulação de pessoas e potenciais interessados,
                            no site em questão, continue a ocorrer.</p>
                    </div>
                </div>
                <div data-aos="fade-right" data-aos-duration="900">
                    <div class="step" style="opacity: 1;">
                        <div class="steps-icon"><img src="https://1.bp.blogspot.com/-By1fRF1MNzE/YE1GQXnjNwI/AAAAAAAAG2M/5jrYzYgjGj0jEOGjgck7cXUXGdFzouSQwCLcBGAsYHQ/s320/social-media.png" height="70" alt=""></div>
                        <div class="headline-7">Social Media</div>
                        <p>
                            O social media é o profissional responsável por atualizar,
                            gerar e monitorar conteúdos para as páginas
                            oficiais de uma marca ou empresa nas redes sociais. Criatividade,
                            pontualidade, Planejamento estratégico e análise de resultados são também requisitos
                            e funções atribuídas ao social media.
                        </p>
                    </div>
                </div>
            </div>
            <div data-aos="fade-left" data-aos-duration="900">
                <div class="step" style="opacity: 1;">
                    <div class="steps-icon"><img src="https://1.bp.blogspot.com/-STnq3ISyBzY/YE1GPskQw3I/AAAAAAAAG2E/DTYncXNNPaw2ee0vYOWQm3D1R5P09BpDwCLcBGAsYHQ/s320/lamp.png" height="70" alt=""></div>
                    <div class="headline-7">Mentoria</div>
                    <p>A mentoria serve para orientar profissionais na construção e direcionamento da carreira.
                        O objetivo da mentoria é produzir conhecimentos e troca de ideias entre o mentor e o mentorado,
                        de modo que o desenvolvimento profissional dele seja estimulado. Durante o processo,
                        o mentor atua como fonte de inspiração e referência.</p>
                </div>
            </div>
            <div data-aos="fade-left" data-aos-duration="1000">
                <div class="step" style="opacity: 1;">
                    <div class="steps-icon"><img src="https://1.bp.blogspot.com/-oS7Jf4fZu5Y/YE1GQyjcj8I/AAAAAAAAG2Q/A-Ind1xQ31EH68H8z_OuD83BgGP3Y30dQCLcBGAsYHQ/s320/video.png" height="70" alt=""></div>
                    <div class="headline-7">Videos Institucionais</div>
                    <p>O propósito do Vídeo Institucional é criar uma boa imagem e contar uma história
                        a respeito da sua empresa, produto ou serviço. O vídeo institucional, em geral,
                        tem como público-alvo clientes, parceiros, investidores, doadores,
                        público interno, enfim, potenciais consumidores ou investidores.</p>
                </div>
            </div>
            <div data-aos="fade-down-left" data-aos-duration="900">
                <div class="step last" style="opacity: 1;">
                    <div class="steps-icon"><img src="https://1.bp.blogspot.com/-smpmHcEB5fA/YE1GPnzOKqI/AAAAAAAAG2A/lR9-TApToBQ278Dpg4X_hyYisFjbDFH8ACLcBGAsYHQ/s320/camera.png" height="70" alt=""></div>
                    <div class="headline-7">Fotografia</div>
                    <p> A fotografia, mais do que um mero registro, é um bem precioso tanto quando está
                        relacionado a contextos históricos – nos ajudando a reter um fato,
                        a entender o mundo por diversas óticas – mas também quando esta é produto e cumpre a
                        função de apresentar ao cliente um serviço, de forma a interessá-lo, comovê-lo.
                        Sendo estética, ética e eficaz.</p>
                </div>
            </div>
            <div data-aos="fade-up-left" data-aos-duration="600">
                <div class="step last" style="opacity: 1;">
                    <div class="steps-icon"><img src="https://1.bp.blogspot.com/-u2CluGvT7KY/YE1GRRxDnAI/AAAAAAAAG2U/au6scZLvPE86oGGpbnyJ-TTL8FMg-MTPQCLcBGAsYHQ/s320/web-design.png" height="70" alt=""></div>
                    <div class="headline-7">Criação de Sites</div>
                    <p>Além de credibilidade, um site é a principal forma para se estabelecer
                        um bom relacionamento com o consumidor,
                        pois é lá que se encontra todo o DNA do seu negócio: a história de sua marca,
                        os serviços prestados e oferecidos, formas de contato e outras opções de interação e venda.</p>
                </div>
