// Animação Sobre Abrir
// 
$('#sobreAbrir').on('click', function() {
    $(window).scrollTop(0);
    $("#trabalhos").fadeIn('600');
    $('#contato').fadeOut('1300');
    $('#servico').fadeOut("700");
    $('#contato').fadeOut('600');
    $('#brands').fadeOut('600');
    $("#templates").fadeOut('600');
    $("#sobre").fadeOut('600');
    $('#IA-MenuBar').fadeIn('200');
})

